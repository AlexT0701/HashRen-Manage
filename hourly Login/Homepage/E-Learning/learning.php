<H1 class="h1"> Workplace Violence </H1>
<p>  Workplace violence is a frustrating problem facing Federal agencies today. While more and more information on the causes of violence and how to handle it is becoming known, there is often no reasonable rationale for this type of conduct and, despite everything we know 
     or do, violent situations happen. No employer is immune from workplace violence and no employer can totally prevent it. 
</p>
<p>  The cost to organizations is staggering. It is impossible to overstate the costs of workplace violence, because a single incident can have sweeping repercussions. There can be the immediate and profound loss of life or physical or psychological repercussions felt by the 
     victim as well as the victim's family, friends, and co-workers; the loss of productivity and morale that sweeps through an organization after a violent incident; and the public relations impact on an employer when news of violence reaches the media. Workplace violence 
     affects other areas as well. The adverse impact on organizations and individuals is wide-ranging and can include:
</p>

<ul>
   <li>
      <p> Temporary/Permanent Absence of Skilled Employee </p>
   </li>
   <li>
      <p> Psychological Damage </p>
   </li>
   <li>
      <p> Property Damage, Theft, and Sabotage </p>
   </li>
   <li>
      <p> Productivity Impediments </p>
   </li>
   <li>
      <p> Diversion of Management Resource </p>
   </li>
   <li>
      <p> Increased Security Costs </p>
   </li>
   <li>
      <p> Increased Workers' Compensation Costs </p>
   </li>
   <li>
      <p> Increased Personnel Costs </p>
   </li>
   <li>
      <p> Temporary/Permanent Absence of Skilled Employee </p>
   </li>
</ul>

<p>  There are many theories about the causes of workplace violence. However, caution should be taken when profiling or stereotyping individuals or organizations since the presence of any of the factors related to these theories does not necessarily indicate a violent act will be 
     carried out. Nevertheless, an incident can be the result of any one or a combination of these factors. Remember – violence or threats of violence in all forms is unacceptable workplace behavior. It will not be tolerated and it will be dealt with appropriately.
</p>

<H2 class="h2"> Policy, Purpose, and Scope </H2>
<p>  Every year, approximately two million people throughout the country are victims of non-fatal violence at the workplace. Officials at the Department of Justice have found violence to be a leading cause of fatal injuries at work with about 1,000 workplace homicides each year. 
     Violence against employees occurs in a variety of circumstances and situations including: robberies and other crimes, actions by frustrated or dissatisfied clients and customers, acts perpetrated by disgruntled co-workers or former co-workers, and domestic incidents that 
     spill over into the workplace.
</p>
<p>  The U.S. Department of Labor's (DOL) policy and position on workplace violence are clear. It is our policy to promote a safe environment for our employees and the visiting public, and to work with our employees to maintain a work environment that is free from violence, 
     harassment, intimidation, and other disruptive behavior. The Department's position in this area is that violence or threats of violence — in all forms — is unacceptable behavior. It will not be tolerated and will be dealt with appropriately.
</p>
<p>  The primary purpose of this document is to provide employees of the Department with a concise reference regarding the Department's program on managing actual and/or potentially violent situations. It is intended to make employees, including supervisors and managers, 
     aware of the potential for violence in the workplace, to increase their abilities to recognize early warning signs of potentially violent situations, and to understand how to respond to actual or potential incidents. It identifies functional area experts who employees, 
     supervisors, and managers can call on to help them assess, defuse, and/or resolve the situation. This document also provides some prevention tips. Finally, some additional resources are included in the Appendices for those who want to learn more.
</p>
<p>  All employees and all facilities of the Department are covered by the policies and program guidance contained in this document. The Department's policy also applies to contractors and visitors to Department facilities.
</p>
<p>  This document appears on the Department's LaborNet under "Reference Library" and portions of the document are being condensed into one or more "Desk Aids." All DOL employees should read and become familiar with the information provided in this document.
</p>

<H2 class="h2"> Roles and Responsibilities </H2>
<p>  The goal of the DOL Workplace Violence Program is to support a work environment in which violent or potentially violent situations are effectively addressed with a focus on prevention by increasing employee understanding of the nature of workplace violence, how to respond 
     to it, and how to prevent it. Success in the protection of our employees requires your personal attention and, as necessary, appropriate action.
</p>
<p>  <strong>IT IS UP TO EACH EMPLOYEE TO HELP MAKE THE DEPARTMENT A SAFE WORKPLACE FOR ALL OF US.</strong> The expectation is that each employee will treat all other employees, as well as customers or clients, with dignity and respect.
</p>
<p>  Depending on the parameters of the incident and the resources available, one or more of the experts in the functional areas listed below may be called upon to provide technical assistance in their particular field to help assess, investigate, and/or respond to a violent or 
     potentially violent situation.
</p>

<p>  <strong>Employees (Including Managers and Supervisors) are responsible for:</strong> 
</p>
<ul>
   <li>
      <p> their own behavior by interacting responsibility with fellow employees, supervisors, and clients; </p>
   </li>
   <li>
      <p> being familiar with Department policy regarding workplace violence; </p>
   </li>
   <li>
      <p> promptly reporting actual and/or potential acts of violence to appropriate authorities; </p>
   </li>
   <li>
      <p> cooperating fully in investigations/assessments of allegations of workplace violence; </p>
   </li>
   <li>
      <p> being familiar with the service provided by the Employee Assistance Program; and </p>
   </li>
   <li>
      <p> informing appropriate personnel about restraining or protective court orders related to domestic situations so that assistance can be offered at the work site. </p>
   </li>
</ul>

<p>  <strong>Managers and Supervisors are additionally responsible for:</strong> 
</p>
<ul>
   <li>
      <p> informing employees of the Department's workplace violence policy and program; </p>
   </li>
   <li>
      <p> taking all reported incidents of workplace violence seriously; </p>
   </li>
   <li>
      <p> investigating all acts of violence, threat, and similar disruptive behavior in a timely fashion and taking the necessary action(s); </p>
   </li>
   <li>
      <p> providing feedback to employees regarding the outcome of their reports regarding violent or potentially violent incidents; </p>
   </li>
   <li>
      <p> requesting, where appropriate, assistance from functional area expert(s); </p>
   </li>
   <li>
      <p> being cognizant of situations that have the potential to produce violent behavior and promptly addressing them with all concerned parties; </p>
   </li>
   <li>
      <p> encouraging employees who show signs of stress or evidence of possible domestic violence to seek assistance, such as the Employee Assistance Program; and </p>
   </li>
   <li>
      <p> assuring, where needed, that employees have time and opportunity to attend training, e.g., conflict resolution, stress management, etc. </p>
   </li>
</ul>

<p>  <strong>Security Office and Federal Protective Service are responsible for:</strong>
<ul>
   <li>
      <p> providing security and helping to defuse violent situations; </p>
   </li>
   <li>
      <p> providing technical advice and support regarding physical security matters; </p>
   </li>
   <li>
      <p> maintaining an ongoing security awareness program; </p>
   </li>
   <li>
      <p> assisting with or conducting investigations of threats or incident of violence; </p>
   </li>
   <li>
      <p> requesting, where appropriate, assistance from functional area expert(s); </p>
   </li>
   <li>
      <p> acting as liaison with local authorities and outside law enforcement agencies; and </p>
   </li>
   <li>
      <p> making arrests for acts of violence in Government owned and/or Government leased buildings, when appropriate and when having authority. </p>
   </li>
</ul>

<p>  <strong>Safety and Health Managers are responsibilities for:</strong>
<ul>
   <li>
      <p> assisting, when appropriate, Department officials with threat assessment and response activities; </p>
   </li>
   <li>
      <p> assisting, when appropriate, in assessing the physical and social environment for potential negative and positive stressors (e.g., space, lighting, temperature, and noise level); </p>
   </li>
   <li>
      <p> preparing trend reports and other analyses of safety and health incident report data; and </p>
   </li>
   <li>
      <p> assisting in emergency preparedness activities. </p>
   </li>
</ul>

<p>  <strong>Employee Assistance Program is responsible for:</strong>
<ul>
   <li>
      <p> providing consultation and guidance to supervisors in dealing with employees who exhibit performance or conduct problems; </p>
   </li>
   <li>
      <p> providing problem assessment for employees experiencing personal problems on and off the job; </p>
   </li>
   <li>
      <p> providing short-term counseling and referral service to employees; </p>
   </li>
   <li>
      <p> referring employees needing long-term counseling to appropriate treatment resources; and </p>
   </li>
   <li>
      <p> participating in conducting threat assessments, when requested. </p>
   </li>
</ul>

<p>  <strong>Unions and Their Representatives are responsible for:</strong>
<ul>
   <li>
      <p> supporting the Department's workplace violence policy and program; </p>
   </li>
   <li>
      <p> being cognizant of situations that have the potential to produce violence and promptly addressing them with all concerned parties; </p>
   </li>
   <li>
      <p> being sensitive to stress generated by the workplace and assisting employees in alleviating it; </p>
   </li>
   <li>
      <p> encouraging employees who show signs of stress to obtain assistance, such as that offered by the Employee Assistance Program; and </p>
   </li>
   <li>
      <p> advising employees, if they inquire, of the procedures for reporting violent behavior. </p>
   </li>
</ul>

<p>  <strong>Health Unit is responsible for:</strong>
<ul>
   <li>
      <p> providing first aid/emergency care in a medical emergency and referring clients to appropriate community medical resources, when needed. </p>
   </li>
</ul>

